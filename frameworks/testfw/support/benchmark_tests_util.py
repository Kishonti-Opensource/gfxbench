#!/usr/bin/env python
# encoding: utf-8

import sys
import os
import json

from optparse import OptionParser

__all__ = []
__version__ = 0.1
__date__ = '2015-02-12'
__updated__ = '2015-02-12'

DEBUG = 0
TESTRUN = 0
PROFILE = 0
IMAGE_POSTFIXES = [ '', '_full', '_loading' ]
SEPARATOR = ";"

def main(argv=None):
    '''Command line options.'''

    program_name = os.path.basename(sys.argv[0])
    program_version = "v0.1"
    program_build_date = "%s" % __updated__

    program_version_string = '%%prog %s (%s)' % (program_version, program_build_date)
    #program_usage = '''usage: spam two eggs''' # optional - will be autogenerated by optparse
    program_longdesc = '''''' # optional - give further explanation about what the program does
    program_license = "Copyright 2015 user_name (organization_name)                                            \
                Licensed under the Apache License 2.0\nhttp://www.apache.org/licenses/LICENSE-2.0"

    if argv is None:
        argv = sys.argv[1:]
    try:
        # setup option parser
        parser = OptionParser(version=program_version_string, epilog=program_longdesc, description=program_license)
        parser.add_option("-f", "--file", dest="file", help="set input file [default: %default]", metavar="FILE")
        parser.add_option("-t", "--test_ids", action="store_true", dest="print_test_ids", default=False, help="print test ids")
        parser.add_option("-r", "--result_ids", action="store_true", dest="print_result_ids", default=False, help="print result ids")
        parser.add_option("-i", "--image_ids", action="store_true", dest="print_image_ids", default=False, help="print image ids")
 
        # process options
        (opts, _) = parser.parse_args(argv)
        if not opts.file:
            parser.error('Filename not given')

        json_data=open(opts.file)
        test_ids = []
        result_ids = []
        image_ids = []
        tests = json.load(json_data)
        for test in tests['tests']:
            for postfix in test['variant_postfixes']:
                test_id = "%s%s" % (test['test_name'],postfix)
                test_ids.append(test_id)
                images = [ test_id + i for i in IMAGE_POSTFIXES ]
                image_ids.extend(images)
                for result in test['results']:
                    result_id = "%s%s" % (test_id, result['result_postfix'])
                    result_ids.append(result_id)
                    image_ids.append(result_id)
        
        # remove duplicates
        image_ids = set(image_ids)
        if opts.print_result_ids:
            print( SEPARATOR.join(result_ids))

        if opts.print_test_ids:
            print( SEPARATOR.join(test_ids))
            
        if opts.print_image_ids:
            print( SEPARATOR.join(image_ids))

    except Exception as e:
        indent = len(program_name) * " "
        sys.stderr.write(program_name + ": " + repr(e) + "\n")
        sys.stderr.write(indent + "  for help use --help")
        return 2


if __name__ == "__main__":
    if DEBUG:
        sys.argv.append("-h")
    if TESTRUN:
        import doctest
        doctest.testmod()
    if PROFILE:
        import cProfile
        import pstats
        profile_filename = 'benchmark_tests_util_profile.txt'
        cProfile.run('main()', profile_filename)
        statsfile = open("profile_stats.txt", "wb")
        p = pstats.Stats(profile_filename, stream=statsfile)
        stats = p.strip_dirs().sort_stats('cumulative')
        stats.print_stats()
        statsfile.close()
        sys.exit(0)
    sys.exit(main())
