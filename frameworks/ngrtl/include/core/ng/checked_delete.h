#ifndef NG_CHECKED_DELETE_INCLUDED
#define NG_CHECKED_DELETE_INCLUDED

namespace ng {


//must be prefixed with ng_ otherwise clashes with the boost version because of the argument-dependent lookup
template<class T> inline void
	ng_checked_delete(T * x)
{
    // intentionally complex - simplification causes regressions
    typedef char type_must_be_complete[ sizeof(T)? 1: -1 ];
    (void) sizeof(type_must_be_complete);
    delete x;
}

template<class T> inline void
	ng_checked_array_delete(T * x)
{
    typedef char type_must_be_complete[ sizeof(T)? 1: -1 ];
    (void) sizeof(type_must_be_complete);
    delete [] x;
}

}
#endif

